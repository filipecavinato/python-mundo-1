n = float(input('Digite um valor (em metros): '))

print('{} metros é igual a {:.1f} quilômetros'.format(n, n*0.001))
print('{} metros é igual a {:.1f} hectômetros'.format(n, n*0.01))
print('{} metros é igual a {:.1f} decâmetros'.format(n, n*0.1))
print('{} metros é igual a {:.1f} decímetros'.format(n, n*10))
print('{} metros é igual a {:.1f} centímetros'.format(n, n*100))
print('{} metros é igual a {:.1f} milímetros'.format(n, n*1000))
